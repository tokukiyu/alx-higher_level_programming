def add(a, b):
    """My addition function

    Args:
    a: firdt integer
    b: second integer

    Returns:
    The value. a + b
    """
    return (a + b)

def sub(a, b):
    """My substraction function

    Args:
    a: first integer
    b: second integer

    Returns:
    The return value. a - b
    """
    return (a - b)

def mul(a, b):
    """My multiplication function

    Args:
    a: first integer
    b: second integer

    Returns:
    The return value. a *b
    """
    return (a * b)

def div(a, b):
    """My divistion function

    Args:
    a: first integer
    b: second integer

    Return:
    The return value. a / b
    """
    return (a / b)
